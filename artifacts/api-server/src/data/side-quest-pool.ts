export function getRandomSideQuests(count: number, seed: number): SideQuestTemplate[] {
  const shuffled = [...SIDE_QUEST_POOL].sort(() => {
    const x = Math.sin(seed++) * 10000;
    return x - Math.floor(x) - 0.5;
  });
  return shuffled.slice(0, count);
}

export function getDailySideQuests(date: Date): SideQuestTemplate[] {
  const dayOfYear = Math.floor((date.getTime() - new Date(date.getFullYear(), 0, 0).getTime()) / 86400000);
  return getRandomSideQuests(3, dayOfYear * 7919);
}

export function getWeeklySideQuests(date: Date): SideQuestTemplate[] {
  const weekOfYear = Math.floor(
    (date.getTime() - new Date(date.getFullYear(), 0, 0).getTime()) / (86400000 * 7)
  );
  return getRandomSideQuests(5, weekOfYear * 3571);
}
